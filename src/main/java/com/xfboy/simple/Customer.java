package com.xfboy.simple;

/**
 * Customer
 *
 * @author alex.chen
 * @version 1.0.0
 * @date 2016/8/6
 */
public class Customer {
    private String name;
    private int age;
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    @Override
    public String toString() {
        return "Customer{" +
                "name='" + name + '\'' +
                ", age=" + age +
                '}';
    }
}
